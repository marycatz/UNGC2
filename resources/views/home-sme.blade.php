            <h2 class="post-title bold"><b>ACCESS Introduction</b></h2>
            <p>The first challenge for Corporates is how to assess their SME suppliers’ sustainability performance. Most sustainability assessment tools are focused on Corporates and are not fit for SME purpose. In order to address the need to assess and improve SME sustainability performance, the SME ESG Assessment tool has been developed by UN Global Compact Network Mala​ysia & Brunei in collaboration with the Nottingham University Business School, University of Nottingham Malaysia, to assess SMEs sustainability performance. It considers the most relevant indicators that are applicable to the specific business environment of an SME. <br> <br>

                Once the SME ESG assessment has been completed, the results page would identify the gaps and indicators that will require improvement. It also enables corporate buyers to identify capacity building opportunities for their suppliers and to construct a more resilient supply chain.
            
